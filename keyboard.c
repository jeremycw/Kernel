void keyboard_handler()
{
	static int shift = 0;

    unsigned char c;

    c = inportb(0x60);

    if(c & 0x80)
    {
        if(c == 0xAA)
            shift = 0;
        if(c == 0xB8)
            alt = 0;
        if(c == 0x9D)
            ctrl = 0;
    }
    else
    {
        if(c == 0x2A || c == 0x36)
            shift = 1;
        else if(c == 0x38)
            alt = 1;
        else if(c == 0x1D)
            ctrl = 1;
        else
        {
            if(!shift)
                OS_Write(cfifo, kbdus[c]);
            else
                OS_Write(cfifo, kbsft[c]);
        }
    }
    outportb(0x20, 0x20);
}

unsigned char getchar()
{
	int v;
    while(!OS_Read(cfifo, &v))
        OS_Yield();
    return v;
}

int is_alt()
{
	if(alt)
		return 1;
	return 0;
}

int is_ctrl()
{
	if(ctrl)
		return 1;
	return 0;
}
